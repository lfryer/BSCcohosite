<div id="accordion" class="ui-accordion">
  <h3 class="ui-accordion-header" id="header">Bay State Commons Vision Statement</h3>
  <div class="ui-accordion-content">
    <p id="header_content">As written during visioning workshop facilitated by Kraus-Fitch Architects on November 17, 2013</p>
    <p>
    We intend to create a cohousing community which fosters group cohesiveness and serendipitous social interaction between neighbors.
    <br>
    We envision a community in Bostonâ€™s Metro North area with a small-town character and an emphasis on beauty in both architecture and landscape.
    <br>
    We value a balance between the private and the shared, with individually-owned homes supplemented by extensive common facilities and shared green space.
    <br>
    We seek a diverse, multi-generational community with residents who are eager to participate in both design and governance.
    </p>
  </div>
  <h3 class="ui-accordion-header">We promote an environment that encourages social serendipity:</h3>
  <div class="ui-accordion-content">
    <ul>
    <li>We will design our community to facilitate unplanned socializing.</li>
    <li>We aim to build a community which enables a natural and easy transition from private space to shared community space.</li>
    </ul>
  </div>
  <h3 class="ui-accordion-header">We strike a balance between private lives and community needs:</h3>
  <div class="ui-accordion-content">
    <ul>
    <li>We prioritize the needs of the individual in private spaces, and the needs of the community in public spaces.</li>
    <li>We respect the privacy of the home.</li>
    <li>Our community will make space for both privately owned homes and shared common facilities.</li>
    </ul>
  </div>
  <h3 class="ui-accordion-header">We promote a cohesive community:</h3>
  <div class="ui-accordion-content">
    <ul>
    <li>We think of our community as a family of choice.</li>
    <li>We plan to have community meals on a regular basis.</li>
    <li>Our members enrich each other through exchange of knowledge, shared projects, and hobbies.</li>
    <li>Our community shares in music and celebration.</li>
    <li>Our community is defined by our care and respect for one another and for our common spaces.</li>
    </ul>
  </div>
  <h3 class="ui-accordion-header">We foster participation in our community:</h3>
  <div class="ui-accordion-content">
    <ul>
    <li>We follow an efficient consensus-based decision making model.</li>
    <li>We promote good communication and conflict resolution.</li>
    <li>We encourage our members to participate in the design process.</li>
    <li>We plan to welcome participation of renters in community governance.</li>
    </ul>
  </div>
  <h3 class="ui-accordion-header">We promote diversity in our community:</h3>
  <div class="ui-accordion-content">
    <ul>
    <li>Our community will be inclusive to people of different social, economic, generational, and racial backgrounds, as well as all sexual orientations and gender identities.</li>
    <li>We will provide a range of unit sizes and prices.</li>
    <li>We will strive for cost efficiency for individual units.</li>
    <li>We will provide accessibility to differently abled people. </li>
    </ul>
  </div>
  <h3 class="ui-accordion-header">We promote sustainability in our community:</h3>
  <div class="ui-accordion-content">
  	<ul>
  	<li>We support an ecologically sustainable lifestyle.</li>
    <li>We strive for energy efficiency in our community buildings.</li>
    <li>We will reduce redundancy by sharing selected resources and costly equipment. (We are not an income-sharing community.)</li>
    <li>We strive to reduce fuel use through car sharing and a bike-friendly atmosphere.</li>
    </ul>
  </div>
  <h3 class="ui-accordion-header">We picture a small-town character:</h3>
  <div class="ui-accordion-content">
  	<ul>
  	<li>We strive for a warm and welcoming atmosphere for both residents and guests.</li>
    <li>Our community will be a safe and nurturing environment in which to raise children.</li>
    <li>Our ideal community size is 20-30 units.</li>
    <li>Some or all units will have immediate access to outdoors (private entrances).</li>
    <li>We will use landscaping and architecture to create a peaceful captive green space full of natural beauty.</li>
    </ul>
  </div>
  <h3 class="ui-accordion-header">We intend to place our community in a convenient location:</h3>
  <div class="ui-accordion-content">
  	<ul>
  	<li>Our community will have access to quality public schools.</li>
    <li>The commuting time to Park Street station will be under 1 hour.</li>
    <li>Our community will be within one mile of regular public transit.</li>
    <li>Our community will have easy access, by foot or bicycle, to basic services and downtown areas.</li>
    <li>Our community will be within biking distance to conservation land (if possible).</li>
    </ul>
  </div>
</div>